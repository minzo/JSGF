(function( window, undefined ){

//------------------------------------------------------------------------------
// コンポーネント
//------------------------------------------------------------------------------
Component = class
{
    //--------------------------------------------------------------------------
    // コンストラクタ
    //--------------------------------------------------------------------------
    constructor() { this.owner = null; }

    //--------------------------------------------------------------------------
    // 計算処理 - Component は calc() が必ず呼ばれます
    // @param owner このコンポーネントをアタッチしているオブジェクト
    //--------------------------------------------------------------------------
    calc( owner ) { this.owner = owner; }
};

})( window );
